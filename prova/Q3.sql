-- Evolution of the avg_score

SELECT
    ad.area_name,
    dd.inspection_year,
    ROUND(AVG(fi.score_assigned), 2) AS avg_score
FROM
    fact_inspection AS fi
JOIN
    area_dim AS ad ON ad.area_key = fi.area_key
JOIN
    date_dim AS dd ON dd.date_key = fi.date_key
GROUP BY
    ad.area_name, dd.inspection_year
ORDER BY
    ad.area_name, dd.inspection_year ASC

